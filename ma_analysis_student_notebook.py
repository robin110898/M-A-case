print("="*80)

# Run diagnostics for each ratio
for i, ratio in enumerate(individual_ratios):
    print(f"\n📊 [{i+1}/9] ANALYZING: {ratio}")
    print("-" * 60)

    # Get target variable
    y_data = df_clean[ratio]

    # Fit regression model
    try:
        model = LinearRegression()
        model.fit(X_data, y_data)

        # Calculate metrics
        y_pred = model.predict(X_data)
        residuals = y_data - y_pred
        r_squared = model.score(X_data, y_data)

        # Initialize results storage
        diagnostics = {
            'ratio': ratio,
            'r_squared': r_squared,
            'n_obs': len(y_data)
        }

        print(f"📈 Model Performance: R² = {r_squared:.3f} ({r_squared*100:.1f}% variance explained)")

        # ===== TEST 1: VIF (Multicollinearity) =====
        # This is the same for all ratios since we use same predictors
        if i == 0:  # Only calculate once
            print(f"\n🔬 TEST 1: VIF (Multicollinearity Check)")
            vif_scores = {}
            for j, var in enumerate(X_vars):
                y_temp = X_data[var]
                X_temp = X_data.drop(columns=[var])
                model_temp = LinearRegression()
                model_temp.fit(X_temp, y_temp)
                r_squared_temp = model_temp.score(X_temp, y_temp)
                vif = 1 / (1 - r_squared_temp) if r_squared_temp < 0.999 else float('inf')
                vif_scores[var] = vif
                print(f"   {var}: VIF = {vif:.2f} {'✅' if vif < 5 else '⚠️' if vif < 10 else '❌'}")

            avg_vif = np.mean(list(vif_scores.values()))
            max_vif = max(vif_scores.values())
            vif_assessment = "✅ Excellent" if max_vif < 5 else "⚠️ Moderate" if max_vif < 10 else "❌ High"
            print(f"   📊 Overall: Avg VIF = {avg_vif:.2f}, Max VIF = {max_vif:.2f} ({vif_assessment})")
            diagnostics['vif_scores'] = vif_scores
            diagnostics['avg_vif'] = avg_vif
            diagnostics['max_vif'] = max_vif
        else:
            # Use VIF from first ratio (same predictors)
            diagnostics['vif_scores'] = comprehensive_diagnostics[individual_ratios[0]]['vif_scores']
            diagnostics['avg_vif'] = comprehensive_diagnostics[individual_ratios[0]]['avg_vif']
            diagnostics['max_vif'] = comprehensive_diagnostics[individual_ratios[0]]['max_vif']
            print(f"🔬 TEST 1: VIF = {diagnostics['max_vif']:.2f} (same predictors as other ratios)")

        # ===== TEST 2: Breusch-Pagan (Heteroskedasticity) =====
        print(f"🔬 TEST 2: Breusch-Pagan (Heteroskedasticity)")
        try:
            # Add constant column for Breusch-Pagan test (required by statsmodels)
            X_with_constant = sm.add_constant(X_data)
            lm_stat, lm_pvalue, fvalue, f_pvalue = het_breuschpagan(residuals, X_with_constant)
            het_result = "✅ No heteroskedasticity" if lm_pvalue > 0.05 else "❌ Heteroskedasticity detected"
            print(f"   LM Statistic = {lm_stat:.3f}, p-value = {lm_pvalue:.4f}")
            print(f"   Result: {het_result}")
            diagnostics['heteroskedasticity'] = {
                'lm_stat': lm_stat,
                'p_value': lm_pvalue,
                'result': 'pass' if lm_pvalue > 0.05 else 'fail'
            }
        except Exception as e:
            print(f"   ⚠️ Could not compute Breusch-Pagan test: {str(e)}...")
            diagnostics['heteroskedasticity'] = {'result': 'error'}

        # ===== TEST 3: Shapiro-Wilk (Normality) =====
        print(f"🔬 TEST 3: Shapiro-Wilk (Normality of Residuals)")
        try:
            shapiro_stat, shapiro_pvalue = stats.shapiro(residuals)
            norm_result = "✅ Residuals normal" if shapiro_pvalue > 0.05 else "❌ Non-normal residuals"
            print(f"   W Statistic = {shapiro_stat:.4f}, p-value = {shapiro_pvalue:.4f}")
            print(f"   Result: {norm_result}")
            diagnostics['normality'] = {
                'w_stat': shapiro_stat,
                'p_value': shapiro_pvalue,
                'result': 'pass' if shapiro_pvalue > 0.05 else 'fail'
            }
        except Exception as e:
            print(f"   ⚠️ Could not compute Shapiro-Wilk test: {str(e)[:50]}...")
            diagnostics['normality'] = {'result': 'error'}

        # ===== TEST 4: Durbin-Watson (Autocorrelation) =====
        print(f"🔬 TEST 4: Durbin-Watson (Autocorrelation)")
        try:
            dw_stat = durbin_watson(residuals)

            # Interpret DW statistic
            if dw_stat < 1.5:
                dw_result = "❌ Positive autocorrelation"
            elif dw_stat > 2.5:
                dw_result = "❌ Negative autocorrelation"
            else:
                dw_result = "✅ No autocorrelation"

            print(f"   DW Statistic = {dw_stat:.4f}")
            print(f"   Result: {dw_result}")
            diagnostics['autocorrelation'] = {
                'dw_stat': dw_stat,
                'result': 'pass' if 1.5 <= dw_stat <= 2.5 else 'fail'
            }
        except Exception as e:
            print(f"   ⚠️ Could not compute Durbin-Watson test: {str(e)[:50]}...")
            diagnostics['autocorrelation'] = {'result': 'error'}

        # Overall assessment
        tests_passed = 0
        total_tests = 4

        if diagnostics.get('max_vif', float('inf')) < 5:
            tests_passed += 1
        if diagnostics.get('heteroskedasticity', {}).get('result') == 'pass':
            tests_passed += 1
        if diagnostics.get('normality', {}).get('result') == 'pass':
            tests_passed += 1
        if diagnostics.get('autocorrelation', {}).get('result') == 'pass':
            tests_passed += 1

        diagnostics['tests_passed'] = tests_passed
        diagnostics['pass_rate'] = tests_passed / total_tests

        overall_status = "🏆 Excellent" if tests_passed >= 3 else "✅ Good" if tests_passed >= 2 else "⚠️ Caution"
        print(f"\n📊 OVERALL DIAGNOSTIC STATUS: {overall_status} ({tests_passed}/{total_tests} tests passed)")

    except Exception as e:
        print(f"❌ Error analyzing {ratio}: {str(e)[:100]}...")
        diagnostics = {'ratio': ratio, 'error': str(e)}

    # Store results
    comprehensive_diagnostics[ratio] = diagnostics

