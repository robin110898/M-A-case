    'Current_Ratio_pct', 'Quick_Ratio_pct', 'Cash_Ratio_pct',  # Liquidity
    'Total_Debt_Assets_pct', 'Total_Debt_Equity_pct', 'Net_Debt_Equity_pct'  # Solvency
