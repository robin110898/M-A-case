"""## 🔬 Understanding Statistical Diagnostics

Before we analyze the relationship between M&A activity and financial performance, we need to make sure our statistical models are **trustworthy**. Think of this like checking your car before a long trip - you want to make sure everything works properly!

### Why Statistical Diagnostics Matter:
Just like a recipe needs the right ingredients in the right amounts, statistical models have **assumptions** that need to be met for the results to be reliable.

### The Four Key Tests We'll Perform:

#### 1. 🔗 **Multicollinearity Test (VIF)**
- **What it checks**: Are our predictor variables too similar to each other?
- **Why it matters**: If two variables measure almost the same thing, our model gets confused
- **Example**: If we include both "Revenue" and "Sales" (which are the same thing), the model can't tell which one really matters
- **The test**: Variance Inflation Factor (VIF)
- **Good result**: VIF < 5 (low correlation between variables)

#### 2. 📊 **Heteroskedasticity Test (Breusch-Pagan)**
- **What it checks**: Are our prediction errors consistent across all data points?
- **Why it matters**: Good models should make similar-sized errors for all predictions
- **Example**: The model should be equally accurate for small and large companies
- **The test**: Breusch-Pagan test
- **Good result**: p-value > 0.05 (errors have constant variance)

#### 3. 📈 **Normality Test (Shapiro-Wilk)**
- **What it checks**: Are our model's errors normally distributed (bell-shaped)?
- **Why it matters**: Statistical tests assume errors follow a normal pattern
- **Example**: Most errors should be small, with few very large errors
- **The test**: Shapiro-Wilk test
- **Good result**: p-value > 0.05 (errors are normally distributed)

#### 4. 🔄 **Autocorrelation Test (Durbin-Watson)**
- **What it checks**: Are our errors independent from year to year?
- **Why it matters**: Each year's error shouldn't be influenced by the previous year's error
- **Example**: A bad 2020 prediction shouldn't make 2021 predictions worse
- **The test**: Durbin-Watson statistic
- **Good result**: DW ≈ 2 (no autocorrelation)

### What We'll Do:
1. Build regression models
2. Run all four diagnostic tests
3. Interpret the results in plain English
4. Identify which models are most trustworthy

Let's start by building the functions we need! 🚀

## 🔗 Variance Inflation Factor (VIF) Analysis

The **VIF test** helps us detect if our predictor variables are too similar to each other (multicollinearity). When variables are highly correlated, it's hard to tell which one really affects our outcome.

### Real-World Example:
Imagine trying to predict house prices using both:
- "Square footage of the house"
- "Number of rooms in the house"

These are highly correlated (bigger houses have more rooms), so the model gets confused about which one really drives price.

### VIF Interpretation Guide:
- **VIF < 5**: ✅ **Low multicollinearity** - Variables are sufficiently independent
- **VIF 5-10**: ⚠️ **Moderate multicollinearity** - Some concern, but often acceptable
- **VIF > 10**: ❌ **High multicollinearity** - Serious problem, remove some variables

Let's test our M&A variables for multicollinearity:
"""

# Function to calculate VIF scores for multicollinearity detection
def calculate_vif_scores(X_df, description=""):
    """
    Calculate Variance Inflation Factor (VIF) to detect multicollinearity.

    Parameters:
    - X_df: DataFrame with predictor variables
    - description: Description of what we're testing

    Returns:
    - Dictionary with VIF results and interpretation
    """
    print(f"🔍 Calculating VIF for {description}...")

    # Calculate VIF for each variable
    vif_data = pd.DataFrame()
    vif_data["Variable"] = X_df.columns
    vif_data["VIF"] = [variance_inflation_factor(X_df.values, i)
                       for i in range(len(X_df.columns))]

    # Get summary statistics
    max_vif = vif_data["VIF"].max()
    avg_vif = vif_data["VIF"].mean()

    # Interpret results
    if max_vif > 10:
        status = "❌ High multicollinearity - PROBLEMATIC"
        color = "red"
    elif max_vif > 5:
        status = "⚠️ Moderate multicollinearity - CONCERNING"
        color = "orange"
    else:
        status = "✅ Low multicollinearity - GOOD"
        color = "green"

    # Display results
    print(f"\n📊 VIF Results for {description}:")
    print("-" * 50)
    for _, row in vif_data.iterrows():
        vif_val = row['VIF']
        var_name = row['Variable']
        if vif_val > 10:
            flag = "❌"
        elif vif_val > 5:
            flag = "⚠️"
        else:
            flag = "✅"
        print(f"   {var_name:<25}: {vif_val:>6.2f} {flag}")

    print(f"\n🎯 Summary:")
    print(f"   Maximum VIF: {max_vif:.2f}")
    print(f"   Average VIF: {avg_vif:.2f}")
    print(f"   Assessment: {status}")

    return {
        'vif_data': vif_data,
        'max_vif': max_vif,
        'avg_vif': avg_vif,
        'status': status,
        'multicollinearity_issue': max_vif > 10
    }

