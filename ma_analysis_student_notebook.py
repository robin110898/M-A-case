"""## 📊 Individual Coefficient Testing (t-tests)

Even though our overall F-test wasn't significant, it's still valuable to examine **individual coefficients** to understand which M&A variables might have effects (even if collectively they don't reach significance).

### What t-tests Tell Us:
While the F-test asks "Does the model explain anything?", t-tests ask:
- "Does **this specific variable** have a significant effect?"
- "If we change this variable by 1 unit, what happens to the outcome?"

### For Each Variable, We Test:
- **Null Hypothesis (H₀)**: This variable has NO effect (coefficient = 0)
- **Alternative Hypothesis (H₁)**: This variable HAS an effect (coefficient ≠ 0)

### Key Information from t-tests:
1. **Coefficient Value**: The actual effect size (positive or negative)
2. **t-statistic**: How many standard errors the coefficient is from zero
3. **p-value**: Probability this effect occurred by chance
4. **Confidence Interval**: Range where the true effect likely falls

### Business Translation:
- **Positive Coefficient**: This M&A variable increases the financial metric
- **Negative Coefficient**: This M&A variable decreases the financial metric
- **p-value < 0.05**: We're confident this effect is real (not due to chance)

Let's examine each M&A variable's individual effect on ROA:
"""

# Function to analyze individual coefficients with t-tests
def run_coefficient_tests(model_result, description=""):
    """
    Perform t-tests for individual coefficient significance

    Parameters:
    - model_result: Result from our analyze_single_model function
    - description: Description for output
    """
    print(f"📊 Individual Coefficient Analysis: {description}")
    print("-" * 60)

    try:
        ols_model = model_result['ols_model']
        X_df = model_result['X_data']

        # Variable names including intercept
        var_names = ['Intercept'] + list(X_df.columns)

        print(f"🔍 Testing Each Variable's Individual Effect:")
        print("-" * 60)

        coef_results = {}

        for i, var in enumerate(var_names):
            # Extract coefficient information
            coef = ols_model.params[i]
            t_stat = ols_model.tvalues[i]
            p_val = ols_model.pvalues[i]

            # Get confidence interval
            try:
                conf_int = ols_model.conf_int()
                ci_lower = conf_int.iloc[i, 0]
                ci_upper = conf_int.iloc[i, 1]
                ci = [ci_lower, ci_upper]
            except:
                ci = [None, None]

            # Determine significance
            is_significant = p_val < 0.05
            sig_symbol = "✅" if is_significant else "❌"

            # Interpret direction and magnitude
            if var == 'Intercept':
                interpretation = f"Baseline value when all M&A variables = 0"
            else:
                direction = "increases" if coef > 0 else "decreases"
                interpretation = f"1 unit increase {direction} outcome by {abs(coef):.4f}"

            print(f"\n📋 {var}:")
            print(f"   Coefficient: {coef:>8.4f}")
            print(f"   t-statistic: {t_stat:>8.3f}")
            print(f"   p-value:     {p_val:>8.4f} {sig_symbol}")
            if ci[0] is not None:
                print(f"   95% CI:      [{ci[0]:>7.4f}, {ci[1]:>7.4f}]")
            print(f"   Effect:      {interpretation}")

            # Store results
            coef_results[var] = {
                'coefficient': coef,
                't_statistic': t_stat,
                'p_value': p_val,
                'significant': is_significant,
                'confidence_interval': ci,
                'interpretation': interpretation
            }

        # Summary of significant effects
        print(f"\n" + "="*60)
        print("🎯 SUMMARY OF SIGNIFICANT EFFECTS:")
        print("="*60)

        significant_vars = [var for var, results in coef_results.items()
                          if results['significant'] and var != 'Intercept']

        if significant_vars:
            print(f"✅ Variables with significant effects ({len(significant_vars)}):")
            for var in significant_vars:
                coef = coef_results[var]['coefficient']
                p_val = coef_results[var]['p_value']
                direction = "↗️ Positive" if coef > 0 else "↘️ Negative"
                print(f"   • {var}: {direction} effect (p={p_val:.3f})")
        else:
            print("❌ No individual variables show significant effects")
            print("   • This aligns with our non-significant F-test")
            print("   • None of the M&A variables individually predict ROA")

        non_significant_vars = [var for var, results in coef_results.items()
                               if not results['significant'] and var != 'Intercept']

        if non_significant_vars:
            print(f"\n❌ Variables with non-significant effects ({len(non_significant_vars)}):")
            for var in non_significant_vars:
                p_val = coef_results[var]['p_value']
                print(f"   • {var}: p={p_val:.3f}")

        return coef_results

    except Exception as e:
        print(f"❌ Error in coefficient analysis: {str(e)}")
        return {}

